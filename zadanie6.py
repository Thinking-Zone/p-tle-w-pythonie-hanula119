# Pętla 'for' jest używana, gdy znamy liczbę iteracji lub gdy chcemy przejść przez wszystkie elementy w sekwencji (np. lista, zakres).
# Pętla 'for' działa na zmiennych iteracyjnych (np. liczbach, elementach listy) i automatycznie kończy działanie po przejściu przez wszystkie elementy.
# Przykład:
# for i in range(5):  # Pętla wykona się 5 razy (i przyjmie wartości 0, 1, 2, 3, 4)
#     print(i)

# Pętla 'while' jest używana, gdy nie znamy dokładnej liczby iteracji i chcemy, aby pętla działała, dopóki spełniony jest określony warunek.
# Pętla 'while' może trwać dowolnie długo, aż warunek przestanie być prawdziwy, a jej zakończenie zależy od spełnienia tego warunku.
# Przykład:
# i = 0
# while i < 5:  # Pętla będzie trwała, dopóki i jest mniejsze niż 5
#     print(i)
#     i += 1  # Należy pamiętać o modyfikacji zmiennej 'i', aby uniknąć nieskończonej pętli.

# Podsumowanie:
# - Pętla 'for' jest idealna, gdy znamy liczbę iteracji lub mamy określoną sekwencję do przeiterowania.
# - Pętla 'while' jest używana, gdy chcemy, aby pętla trwała dopóki jakiś warunek jest prawdziwy.
